                        <h3>ANTIPLAQUETARES:</h3><br />
                        <ul>
                            <li>Existem diferentes tipos de medicamentos Antiplaquetários e ainda que cada um atue de uma forma diferente, todos eles evitam que as plaquetas se aglutinem e formem coágulos.⁶</li>
                            <li>O Antiplaquetar mais comúm é o ácido acetilsalicílico ou AAS, que ajuda a prevenir que se formem coágulos, mas não é um anticoagulante.⁷</li>
                            <li>Os medicamentos antiplaquetares podem ser utilizados principalmente para: prevenir um ataque cardíaco ou AVC em pessoas com doença periférica arterial, para prevenir doenças cardíacas primarias e secundárias e tratar ataques cardíacos.⁸</li>
                            <li>Os efeitos colaterais destes medicamentos podem incluir: Diarreia, coceira, dor de estômago, erupção cutânea e nauseas.⁹</li>
