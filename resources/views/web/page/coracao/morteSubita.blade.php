                <h5 class="co-red mb-3">O que é Morte súbita cardíaca?</h5>
                <p>A Morte súbita cardíaca é uma emergência médica grave e com risco à vida. Durante a Morte súbita
                    cardíaca, a função cardíaca para - abruptamente e sem aviso. Isso causa uma rápida perda de
                    consciência (desmaio). Sem tratamento imediato com desfibrilação (um choque elétrico no coração),
                    podem ocorrer danos cerebrais e morte.</p>
                <p>As definições de Morte súbita cardíaca e ataque cardíaco são completamente diferentes. Um ataque
                    cardíaco é um problema de "encanamento" causado por um ou mais bloqueios nos vasos sanguíneos do
                    coração que impedem o fluxo adequado. Uma pessoa que está tendo um ataque cardíaco está acordada e
                    respirando.</p>
                <p>A Morte súbita cardíaca é definida como um problema elétrico, causado por uma arritmia (batimento
                    cardíaco irregular) que impede o coração de bombear sangue para o cérebro e órgãos vitais. Uma
                    pessoa com Morte súbita cardíaca está inconsciente e sem respiração.</p>
                <p>A cada 39 segundos, alguém morre de morte súbita cardíaca2 - 2. American Heart Association. Heart
                    Disease and Stroke Statistics - 2014 Update. Circulation. 2014; 129:e28-292</p>

                <h5 class="co-red mb-3">O que causa a Morte súbita cardíaca?</h5>

                <p>A Morte súbita cardíaca pode atingir pessoas de qualquer idade, gênero, raça e até mesmo aquelas que
                    aparentam estar com boa saúde. Sabe-se que atletas profissionais de classe mundial no auge da forma
                    física morrem subitamente durante eventos esportivos. Geralmente, isso é resultado de uma Morte
                    súbita cardíaca.</p>
                <p>A maioria dos casos de Morte súbita cardíaca é causada por um tipo de batimento cardíaco irregular
                    (ou arritmia), conhecido como fibrilação ventricular. Os ventrículos são as câmaras inferiores do
                    coração. Durante a fibrilação ventricular, essas câmaras batem muito rápida e irregularmente. Isso
                    faz com que pouco ou nenhum sangue seja bombeado pelo corpo. Se não for tratada em alguns minutos,
                    pode ocorrer morte.</p>
                <p>Outras causas de Morte súbita cardíaca podem ser o resultado de problemas com o sistema elétrico do
                    coração. Se os sinais elétricos do coração ficarem lentos ou pararem, ou se o músculo cardíaco não
                    responder a esses sinais elétricos, pode ocorrer uma Morte súbita cardíaca.</p>


