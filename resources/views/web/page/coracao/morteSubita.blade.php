                <h3 class="co-white">Tratamentos</h3>

                <p class="co-white">A Morte súbita cardíaca é uma emergência médica. A maioria das pessoas que sofre uma
                    Morte súbita cardíaca morre por causa disso - geralmente em minutos. O tratamento imediato com
                    reanimação cardiopulmonar (RCP) e um desfibrilador (um dispositivo que envia um choque elétrico para
                    o coração) pode salvar vidas.</p>
                <p class="co-white">Existem dois tipos de desfibriladores:</p>

                <div class="col-5">
                    <ul class="list-style text-left">
                        <li class="text-white">Um desfibrilador externo (AED) é um pequeno dispositivo portátil que usa
                            placas de eletrodo para diagnosticar um ritmo cardíaco anormal e administrar um choque
                            elétrico, se necessário. Desfibriladores externos são utilizados em locais públicos como
                            escolas, aeroportos e hotéis. Espectadores não treinados podem usar esses dispositivos para
                            aplicar um choque elétrico no tórax de alguém com Morte súbita cardíaca.
                        </li>
                        <li class="text-white">Um cardioversor desfibrilador implantável (ICD, sigla do inglês
                            implantable cardioverter defibrillator), como o Sistema S-ICD EMBLEM MRI, é um dispositivo
                            implantado cirurgicamente que pode detectar batimentos cardíacos irregulares ou perigosos e
                            aplicar choques salva-vidas para ajudar a retornar o ritmo ao normal.
                        </li>
                    </ul>
                </div>

