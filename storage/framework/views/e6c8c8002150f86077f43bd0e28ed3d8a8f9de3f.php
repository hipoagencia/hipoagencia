                <p>Um acidente vascular cerebral (AVC) ocorre quando uma parte do cérebro é danificada devido a um bloqueio de um vaso sanguíneo ou à ruptura de um vaso sanguíneo no cérebro. 1
                    Os pacientes com Fibrilação Atrial (F.A.) têm 5 vezes mais risco de sofrer um Acidente Vascular Cerebral (AVC) do que uma pessoa que não sofre desta doença. Se você é um paciente recém-diagnosticado ou está
                    sob essa condição há algum tempo, essas informações são para você.
                </p>

                <b>Quais são os sinais de alerta de um AVC?</b>
                <p>Existem sinais de alerta de um possível AVC nas quais você deve
