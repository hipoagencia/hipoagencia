                <h3>Tratamentos</h3>


                <p class="co-white">
                    Atualmente, não há cura para a distonia, mas há uma variedade de tratamentos disponíveis que podem
                    aliviar os sintomas. A seleção do tratamento varia de paciente para paciente e geralmente depende do
                    tipo e gravidade da distonia. As opções de tratamento típicas incluem terapia medicamentosa,
                    injeções de toxina botulínica e cirurgia. A estimulação cerebral profunda (DBS) pode ser usada para
                    tratar pessoas com distonia grave que falham no tratamento com agentes farmacológicos e injeções de
                    neurotoxina botulínica.
                </p>

                <h3 class="mt-5">Procedimento DBS</h3>

                <p class="co-white">O procedimento DBS inclui um dispositivo médico modesto que envia sinais para o cérebro. Os sinais
                    ajudam a controlar as funções motoras afetadas pelos sintomas de distúrbios do movimento, como
                    tremor, lentidão e rigidez.</p>

                <p class="co-white">O médico colocará um ou dois fios isolados chamados eletrodos no cérebro. Os eletrodos são então
                    conectados ao estimulador (semelhante a um marcapasso), que normalmente é colocado sob a pele no
                    peito. O dispositivo produz impulsos elétricos leves que estimulam uma região específica do cérebro.
                    Isso pode ajudar a regular a sinalização no cérebro, resultando na melhora dos sintomas da distonia.
                    Embora o DBS não seja uma cura, ele pode ajudar a melhorar as experiências do dia-a-dia e a
                    qualidade de vida.</p>

